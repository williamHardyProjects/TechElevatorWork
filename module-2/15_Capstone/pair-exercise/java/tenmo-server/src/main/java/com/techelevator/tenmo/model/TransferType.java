package com.techelevator.tenmo.model;

public class TransferType {
	private Integer typeId;
	private String type;
	
	public TransferType() {}

	public Integer getTypeId() {
		return typeId;
	}

	public void setTypeId(Integer typeId) {
		this.typeId = typeId;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
	
}
